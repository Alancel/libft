#include "libft.h"

int		ft_atoi(const char *str)
{
	int				sign;
	int				i;
	unsigned long	res;

	sign = 1;
	i = 0;
	res = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + (str[i] - '0');
		i++;
		if (sign < 0 && res > 9223372036854775807)
			return (0);
		if (sign > 0 && res > 9223372036854775807)
			return (-1);
	}
	return (res * sign);
}
